import json

def json_to_escaped_string(json_input: str) -> str:
    """
    将输入的 JSON 字符串转为带转义字符的字符串
    """
    # 先解析成字典，确保输入是合法 JSON
    data = json.loads(json_input)
    # 再转成字符串，并加上转义符
    escaped_str = json.dumps(data, ensure_ascii=False)
    # 最外层也用引号包起来
    return f"\"{escaped_str.replace('\"', '\\\"')}\""


